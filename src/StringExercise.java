public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education";
        System.out.println(education);

        String thoughtControl = "We don't need no thought control";
        System.out.println(thoughtControl);

        String check = "Check \"this\" out!, \"s inside of \"s! ";
        System.out.println(check);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String backslash = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backskashes \\\\\\!";
        System.out.println(backslash);
    }
}
